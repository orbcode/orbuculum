static void _COBSpacketRxed( enum COBSPumpEvent e, struct Frame *p, void *param )

{
    struct test *t = ( struct test * )param;

    switch ( e )
    {
        case COBS_EV_NONE:
            fprintf( stderr, "COBS_EV_NONE\n" );
            break;

        case COBS_EV_UNSYNCED:
            fprintf( stderr, "COBS_EV_UNSYNCED\n" );
            break;

        case COBS_EV_NEWSYNC:
            fprintf( stderr, "COBS_EV_NEWSYNC\n" );
            break;

        case COBS_EV_ERROR:
            fprintf( stderr, "COBS_EV_ERROR\n" );

            if ( p->len )
            {
                _printDigits( p->len, p->d );
                fprintf( stderr, "\n" );
            }

            break;

        case COBS_EV_RXEDFRAME:
            printf( "COBS_EV_RXEDFRAME\n" );

            for ( int i = 0; i < p->len; i++ )
            {
                if ( p->d[i] != t->unenc[i] )
                {
                    fprintf( stderr, "*********FAILED\nWanted:" );
                    _printDigits( testSet[i].unencLen, testSet[i].unenc );
                    printf( "\nRecevd:" );
                    _printDigits( p->len, p->d );
                    fprintf( stderr, "\n" );
                    return;
                }
            }

            if ( p->len != t->unencLen )
            {
                fprintf( stderr, "**********LENGTH MISMATCH\n" );
            }
            else
            {
                fprintf( stderr, "OK\n" );
            }

            break;

        default:
            fprintf( stderr, "Unexpected event %d\n", e );
            break;
    }
}

