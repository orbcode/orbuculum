#define COBS_SYNC_CHAR (0)

// ====================================================================================================
struct COBSDecoder *COBSDecoderInit( struct COBSDecoder *t )

/* Reset a COBSDecoder instance */

{
    if ( !t )
    {
        t = ( struct COBSDecoder * )calloc( 1, sizeof( struct COBSDecoder ) );
        t->selfAllocated = true;
    }

    t->s = COBS_UNSYNCED;
    t->syncMonitor = 0;
    COBSDecoderZeroStats( t );

    return t;
}
// ====================================================================================================
void COBSDecoderDelete( struct COBSDecoder *t )

/* Destroy a COBSDecoder instance, but only if we created it */

{
    if ( t->selfAllocated )
    {
        free( t );
        t = NULL;
    }
}

// ====================================================================================================
void COBSDecoderZeroStats( struct COBSDecoder *t )

{
    memset( &t->stats, 0, sizeof( struct COBSDecoderStats ) );
}
// ====================================================================================================
bool COBSDecoderSynced( struct COBSDecoder *t )
{
    return t->s != COBS_UNSYNCED;
}
// ====================================================================================================
struct COBSDecoderStats *COBSDecoderGetStats( struct COBSDecoder *t )
{
    return &t->stats;
}
// ====================================================================================================
void COBSDecoderForceSync( struct COBSDecoder *t )

/* Force the decoder into a specific sync state */

{
    if ( t->s == COBS_UNSYNCED )
    {
        t->stats.syncCount++;
    }

    t->s = COBS_IDLE;
}
// ====================================================================================================

void COBSPump( struct COBSDecoder *t, uint8_t *incoming, int len,
               void ( *packetRxed )( enum COBSPumpEvent e, struct Frame *p, void *param ),
               void *param )


/* Assemble this packet into a complete frame and call back */

{
    uint8_t *fp = incoming;

    for ( unsigned int rlen = 0; rlen < len; rlen++, fp++ )
    {
        switch ( t->s )
        {
            case COBS_UNSYNCED: // ----------------------------------------------------------------
                if ( COBS_SYNC_CHAR == *fp )
                {
                    packetRxed( COBS_EV_NEWSYNC, NULL, NULL );
                    t->stats.syncCount++;
                    t->s = COBS_IDLE;
                }

                break;

            case COBS_IDLE:  // -------------------------------------------------------------------
                if ( COBS_SYNC_CHAR != *fp )
                {
                    t->f.len = 0;
                    t->intervalCount = *fp;
                    t->maxCount = ( *fp == 255 );
                    t->s = COBS_RXING;
                }

                break;

            case COBS_RXING: // -------------------------------------------------------------------
                t->intervalCount--;

                if ( !t->intervalCount )
                {
                    if ( COBS_SYNC_CHAR == *fp )
                    {
                        /* This is the end of a packet */
                        packetRxed( COBS_EV_RXEDFRAME, &t->f, param );
                        t->s = COBS_IDLE;
                    }
                    else
                    {
                        if ( !t->maxCount )
                        {
                            t->f.d[t->f.len++] = COBS_SYNC_CHAR;
                        }

                        t->intervalCount = *fp;
                        t->maxCount = ( *fp == 255 );
                    }
                }
                else
                {
                    if ( COBS_SYNC_CHAR == *fp )
                    {
                        /* This is the end of a packet and the lengths don't match */
                        t->stats.error++;
                        packetRxed( COBS_EV_ERROR, &t->f, param );
                        t->s = COBS_IDLE;
                    }
                    else
                    {
                        t->f.d[t->f.len++] = *fp;
                    }
                }

                /* Check for frame overflow ... if it's max then error */
                if ( COBS_MAX_PACKET_LEN == t->f.len )
                {
                    t->stats.error++;
                    packetRxed( COBS_EV_ERROR, &t->f, param );
                    t->s = COBS_IDLE;
                }

                break;

            case COBS_ERROR:  // ------------------------------------------------------------------
                t->s = COBS_UNSYNCED;
                t->stats.error++;
                packetRxed( COBS_EV_ERROR, NULL, NULL );
                break;
        }
    }
}


